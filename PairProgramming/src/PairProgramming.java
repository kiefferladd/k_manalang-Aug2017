
public class PairProgramming {

	public static void main(String[] args) {
		Scanner1 = new 
				scanner(system.in);
				boolean done = false;
		int sum = 0;
		while (!done) {
			System.out.println ("Type a number");
			int n = scanner1.nextint();
			sum += n;
			System.out.println("areyoudone?");
			String finished = scanner1.next();
			if (finished.equals("yes")) {
				done = true;
			}
		}
	}
}